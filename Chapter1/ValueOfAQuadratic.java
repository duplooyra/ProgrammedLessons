public class ValueOfAQuadratic
{  
  public static void main ( String[] args )  
  {
       
    double X = 0.0;
    double Result = 3*(X*X)-8*X+4;  
    System.out.println("At X = " + X + " the value is " + Result);
    X = 2.0;
    Result = 3*(X*X)-8*X+4; 
    System.out.println("At X = " + X + " the value is " + Result);
    X = 4.0;
    Result = 3*(X*X)-8*X+4; 
    System.out.println("At X = " + X + " the value is " + Result);
    
    
    
    
  }
}